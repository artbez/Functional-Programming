f x [y] = [x True, y]

1. Так как f имеет 2 параметра, она имеет тип f :: a1 -> a2 -> a3
2. Так как параметр [y] заключен в квадратные скобки, то a2 = [a4] и f :: a1 -> [a4] -> a3
3. Так как результат равен [...], то a3 = [a5]. Таким образом f :: a1 -> [a4] -> [a5]
4. У переменной y тип a4, а результат типа [a5] также содержит y. Отсюда a4 == a5. Итого f :: a1 -> [a4] -> [a4]
5. Результат содержит выражение x True, что является функцией, примененной к одному аргументу, результат которой есть тип a4. Отсюда x имеет тип x :: Bool -> a4. Тогда f :: (Bool -> a4) -> [a4] -> [a4]

Ответ: f :: (Bool -> a) -> [a] -> [a] 






